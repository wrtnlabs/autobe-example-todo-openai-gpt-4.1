import { ArrayUtil, RandomGenerator, TestValidator } from "@nestia/e2e";
import { IConnection } from "@nestia/fetcher";
import typia, { tags } from "typia";

import api from "@ORGANIZATION/PROJECT-api";
import type { IAuthorizationToken } from "@ORGANIZATION/PROJECT-api/lib/structures/IAuthorizationToken";
import type { ITodoListAdmin } from "@ORGANIZATION/PROJECT-api/lib/structures/ITodoListAdmin";
import type { ITodoListTodos } from "@ORGANIZATION/PROJECT-api/lib/structures/ITodoListTodos";

/**
 * Validates that an admin can successfully create a new todo item using the
 * admin-specific endpoint.
 *
 * Steps:
 *
 * 1. Register a new admin via the /auth/admin/join endpoint using random,
 *    valid credentials.
 * 2. As the authenticated admin, prepare a todo creation request with random
 *    valid data (title, optional description, and due date).
 * 3. Call the API to create a todo with POST /todoList/admin/todos.
 * 4. Confirm that the API response matches request data and includes all
 *    required system-managed fields.
 * 5. Assert that the returned todo item is correctly associated with the
 *    current admin (by ownership).
 */
export async function test_api_todo_creation_by_admin(
  connection: api.IConnection,
) {
  // 1. Register new admin and obtain authenticated session
  const admin = await api.functional.auth.admin.join(connection, {
    body: {
      email: typia.random<string & tags.Format<"email">>(),
      password: typia.random<
        string & tags.MinLength<8> & tags.MaxLength<100>
      >(),
    } satisfies ITodoListAdmin.ICreate,
  });
  typia.assert(admin);

  // 2. Prepare todo creation body (use random data for required/optional fields)
  const todoCreate = {
    title: RandomGenerator.paragraph({ sentences: 4 }),
    description: RandomGenerator.paragraph({ sentences: 8 }),
    due_date: new Date(Date.now() + 3 * 24 * 60 * 60 * 1000).toISOString(),
    is_completed: false,
  } satisfies ITodoListTodos.ICreate;

  // 3. Create the todo as admin
  const todo = await api.functional.todoList.admin.todos.create(connection, {
    body: todoCreate,
  });
  typia.assert(todo);

  // 4. Assert data integrity and ownership
  TestValidator.equals("todo title matches", todo.title, todoCreate.title);
  TestValidator.equals(
    "todo description matches",
    todo.description,
    todoCreate.description,
  );
  TestValidator.equals(
    "todo due date matches",
    todo.due_date,
    todoCreate.due_date,
  );
  TestValidator.equals(
    "todo is_completed matches",
    todo.is_completed,
    todoCreate.is_completed,
  );
  TestValidator.equals(
    "todo_list_user_id is admin id",
    todo.todo_list_user_id,
    admin.id,
  );
}

